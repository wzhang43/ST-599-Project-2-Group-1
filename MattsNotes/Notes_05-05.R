### May 5th class notes

- talk to audience, not to slides
- whenever you show a plot, start by explaining the axes and what is being plotted
plots should standalone, caption title or annotate them so subsetting/summarizing is clear

Readability
 - devoe slide to plot. 
- big axis labels (theme_bw(18) ups base size to 18 pt)
- beware pale colors (yellows) disappear
- save plots as pdfs unless file size is large, then use png w/ at least 300 dpi (pdfs dont lose resolution when you scale it. however PDF file size can be an issue.)

